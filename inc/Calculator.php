<?php

namespace Inc;

class Calculator
{

  public function add($num1, $num2)
  {
    return $num1 + $num2;
  }
  public function subtract($num1, $num2)
  {

    return $num1 - $num2;
  }
  public function multiplication($num1, $num2)
  {

    return $num1 * $num2;
  }
  public function devision($num1, $num2)
  {

    return $num1 / $num2;
  }
}